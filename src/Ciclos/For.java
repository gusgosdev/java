package Ciclos;

public class For {
    public static void main(String[] args) {
        // Ciclo for

        // Imprimir n√∫meros del 0 al 10
        int numMin;
        final int NUM_MAX = 10;
        // Empieza el ciclo
        for (numMin = 0; numMin <= NUM_MAX; numMin++) {
            System.out.println(numMin);
        }
    }
}