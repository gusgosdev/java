package Basics.B04_Constantes;

public class Constantes {
    public static void main(String[] args) {
        // No se modifica su valor
        // EL nombre de una constante se escribe con mayusculas (buenas practicas)
        // Si se tiene dos o mas palabras, usamos snake_case

        final int MI_CONSTANTE = 12;
        System.out.println("Mi constante es: " + MI_CONSTANTE);

        // Ejemplo de una constante ya definida en Java
        System.out.println("Constante Math PI: " + Math.PI);
    }
}